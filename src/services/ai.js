function buildClientDataMessage(application) {
  let message = '–î–∞–Ω–Ω—ã–µ –∏–∑ –∞–Ω–∫–µ—Ç—ã –∫–ª–∏–µ–Ω—Ç–∞ (–∑–∞–ø–æ–ª–Ω–µ–Ω–æ —Å–∞–º–∏–º –∫–ª–∏–µ–Ω—Ç–æ–º, –º–æ–∂–µ—Ç —Å–æ–¥–µ—Ä–∂–∞—Ç—å –Ω–µ—Ç–æ—á–Ω–æ—Å—Ç–∏):\n\n';
  message += `–í–æ–∑—Ä–∞—Å—Ç: ${application.age}\n`;
  message += `–¢–∏–ø –∫–æ–∂–∏ (—Å–æ —Å–ª–æ–≤ –∫–ª–∏–µ–Ω—Ç–∞): ${formatSkinType(application.skinType)}\n`;

  if (application.consultationGoal) {
    message += `–¶–µ–ª—å –∫–æ–Ω—Å—É–ª—å—Ç–∞—Ü–∏–∏: ${formatConsultationGoal(application.consultationGoal)}`;
    if (application.consultationGoal === 'ADDITIONAL_PRODUCTS' && application.additionalProducts) {
      message += ` (${application.additionalProducts})`;
    }
    message += '\n';
  }

  message += `–û—Å–Ω–æ–≤–Ω—ã–µ –ø—Ä–æ–±–ª–µ–º—ã: ${application.mainProblems}\n`;

  if (application.priceRange) {
    message += `–ë—é–¥–∂–µ—Ç –Ω–∞ —Å—Ä–µ–¥—Å—Ç–≤–∞: ${formatPriceRange(application.priceRange)}\n`;
  }

  if (application.additionalComment) {
    message += `\n–ö–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π –∫–ª–∏–µ–Ω—Ç–∞ (—Ç–µ–∫—É—â–∏–π —É—Ö–æ–¥, –∞–ª–ª–µ—Ä–≥–∏–∏, –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏):\n${application.additionalComment}\n`;
  }

  message += '\n–ù–∞–ø–∏—à–∏ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ç–µ–ª—å–Ω—ã–π —Å–∫—Ä–∏–ø—Ç –ø–æ —É—Ö–æ–¥—É. –í–º–µ—Å—Ç–æ –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã—Ö –ø—Ä–æ–¥—É–∫—Ç–æ–≤ —É–∫–∞–∂–∏ —Ç–∏–ø —Å—Ä–µ–¥—Å—Ç–≤–∞ –∏ –∞–∫—Ç–∏–≤–Ω—ã–µ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç—ã –≤ —Ñ–æ—Ä–º–∞—Ç–µ: –ë–†–ï–ù–î –Ω–∞–∑–≤–∞–Ω–∏–µ [–°–°–´–õ–ö–ê] ‚Äî –≤—Ä–∞—á –ø–æ—Ç–æ–º –∑–∞–º–µ–Ω–∏—Ç –Ω–∞ —Ä–µ–∞–ª—å–Ω—ã–µ –ø—Ä–æ–¥—É–∫—Ç—ã —Å–æ —Å—Å—ã–ª–∫–∞–º–∏.';
  return message;
}

export async function generateRecommendation(application) {
  const client = getClient();
  if (!client) {
    return { error: 'AI-–ø–æ–º–æ—â–Ω–∏–∫ –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω. –û–±—Ä–∞—Ç–∏—Ç–µ—Å—å –∫ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä—É.' };
  }

  try {
    const messages = [
      { role: 'system', content: buildSystemPrompt() },
      { role: 'user', content: buildClientDataMessage(application) }
    ];

    const response = await client.chat.completions.create({
      model: config.openai.model,
      messages,
      temperature: 0.7,
      max_tokens: 3000
    });

    const text = response.choices[0].message.content;

    return {
      text,
      history: [
        ...messages,
        { role: 'assistant', content: text }
      ],
      usage: response.usage
    };
  } catch (error) {
    console.error('[AI] Error generating recommendation:', error.message);
    return { error: '–û—à–∏–±–∫–∞ AI. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑ –∏–ª–∏ –Ω–∞–ø–∏—à–∏—Ç–µ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏—é –≤—Ä—É—á–Ω—É—é.' };
  }
}

export async function refineRecommendation(history, instruction) {
  const client = getClient();
  if (!client) {
    return { error: 'AI-–ø–æ–º–æ—â–Ω–∏–∫ –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω.' };
  }

  try {
    const messages = [
      ...history,
      { role: 'user', content: `–î–æ—Ä–∞–±–æ—Ç–∞–π —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏—é: ${instruction}` }
    ];

    const response = await client.chat.completions.create({
      model: config.openai.model,
      messages,
      temperature: 0.7,
      max_tokens: 2000
    });

    const text = response.choices[0].message.content;

    return {
      text,
      history: [
        ...messages,
        { role: 'assistant', content: text }
      ],
      usage: response.usage
    };
  } catch (error) {
    console.error('[AI] Error refining recommendation:', error.message);
    return { error: '–û—à–∏–±–∫–∞ AI. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑.' };
  }
}
